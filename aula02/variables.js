// Variáveis
// const são variáveis constante, ou seja, eu não posso alterar seu valor inicial
const myName = "Matheus Rocha"
console.log(myName)

// let e var são variáis que eu posso alterar o valor inicial delas
// let -> escopo mais estrito
let age = 29
console.log(age)

// var -> escopo global
var wife = "Aline Santos"
console.log(wife)